def calculate_perplexity(loss):
    return torch.exp(loss)


def compute_diversity(generated_texts):
    all_words = []
    for text in generated_texts:
        all_words.extend(nltk.word_tokenize(text.lower()))

    # Count number of unique words
    unique_words = set(all_words)

    # Compute type-token ratio
    ttr = len(unique_words) / len(all_words) if all_words else 0

    # Compute word distribution and entropy
    word_counts = Counter(all_words)
    total_words = len(all_words)
    entropy = 0

    for word, count in word_counts.items():
        probability = count / total_words
        entropy -= probability * math.log2(probability)

    return {
        "unique_ratio": ttr,
        "num_unique_words": len(unique_words),
        "total_words": len(all_words),
        "entropy": entropy
    }

def evaluate_generated_text(model, prompts, max_new_tokens=100, temperature=0.7):
    model.eval()
    generated_texts = []

    with torch.no_grad():
        for prompt in prompts:
            context = torch.tensor(enc.encode_ordinary(prompt)).unsqueeze(dim=0).to(device)
            generated = model.generate(context, max_new_tokens, temperature=temperature)
            output_text = enc.decode(generated.squeeze().tolist())
            generated_texts.append(output_text)

    diversity_metrics = compute_diversity(generated_texts)

    if 'wandb' in globals():
        for i, (prompt, text) in enumerate(zip(prompts, generated_texts)):
            wandb.log({
                f"example_{i+1}_prompt": prompt,
                f"example_{i+1}_generated": text
            })

        wandb.log({
            "diversity_unique_ratio": diversity_metrics["unique_ratio"],
            "diversity_entropy": diversity_metrics["entropy"]
        })

    return {
        "generated_texts": generated_texts,
        "diversity": diversity_metrics
    }



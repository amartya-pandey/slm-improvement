# Main training loop
for epoch in tqdm(range(start_epoch, max_iters)):
    # Get batch and train
    X, y = get_batch("train")
    X, y = X.to(device), y.to(device)

    with ctx:
        logits, loss = model(X, y)
        loss_value = loss.item()  # Capture the loss value before division
        loss = loss / gradient_accumulation_steps
        scaler.scale(loss).backward()

    # Track loss for this epoch
    epoch_loss += loss_value
    epoch_steps += 1

    if ((epoch + 1) % gradient_accumulation_steps == 0) or (epoch + 1 == max_iters):
        torch.nn.utils.clip_grad_norm_(model.parameters(), max_norm=0.5)
        scaler.step(optimizer)
        scaler.update()
        optimizer.zero_grad(set_to_none=True)

    scheduler.step()

    # Log to wandb every epoch
    wandb.log({
        "epoch": epoch,
        "step_loss": loss_value,
        "learning_rate": optimizer.param_groups[0]['lr'],
        "epoch_avg_loss": epoch_loss / epoch_steps if epoch_steps > 0 else 0,
    })

    # Evaluation and checkpoint saving (less frequently)
    if epoch % eval_iters == 0 and epoch != 0:
        losses = estimate_loss(model)
        train_loss = losses['train'].item()
        val_loss = losses['val'].item()
        print('+'*50)
        print(f"Epoch {epoch}: train loss {train_loss:.4f}, val loss {val_loss:.4f}")
        print(f"The current learning rate: {optimizer.param_groups[0]['lr']:.5f}")

        train_loss_list += [losses['train']]
        validation_loss_list += [losses['val']]

        # Log evaluation metrics
        wandb.log({
            "eval_epoch": epoch,
            "eval_train_loss": train_loss,
            "eval_val_loss": val_loss,
        })

        # Save latest model checkpoint
        save_checkpoint(
            latest_model_path,
            epoch,
            model,
            optimizer,
            scheduler,
            best_val_loss,
            train_loss_list,
            validation_loss_list
        )

        # Save best model checkpoint if validation loss improved
